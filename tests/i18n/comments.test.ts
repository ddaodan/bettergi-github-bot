import { describe, expect, it } from "vitest";

import { renderAiHelpComment, renderSimilarIssuesComment } from "../../src/i18n/comments.js";

const help = {
  summary: "Example summary.",
  possibleCauses: ["Example cause"],
  troubleshootingSteps: ["Example step"],
  missingInformation: ["Example missing info"]
};

describe("comment renderers", () => {
  it("uses note-style AI hint instead of disclaimer wording", () => {
    const comment = renderAiHelpComment({
      mode: "zh",
      templateKey: "bug",
      help
    });

    expect(comment).toContain("> 注：以上内容由 AI 生成，仅供参考");
    expect(comment).not.toContain("免责声明");
  });

  it("renders question issues with a question-oriented layout in bilingual mode", () => {
    const comment = renderAiHelpComment({
      mode: "zh-en",
      templateKey: "question",
      help
    });

    expect(comment).toContain("## AI 参考回答");
    expect(comment).toContain("### 直接回答");
    expect(comment).toContain("### 判断依据");
    expect(comment).toContain("### 建议下一步");
    expect(comment).toContain("## AI Reference Answer");
    expect(comment).toContain("### Direct Answer");
    expect(comment).toContain("### Key Points");
    expect(comment).toContain("> Note: This response was generated by AI for reference only.");
    expect(comment).not.toContain("### 可能原因");
    expect(comment).not.toContain("### 建议排查步骤");
  });

  it("renders similar issues inside a collapsible block", () => {
    const comment = renderSimilarIssuesComment({
      mode: "zh-en",
      issues: [
        {
          candidate: {
            number: 16,
            title: "[bug] 一条龙设置无法保存",
            body: "",
            labels: ["bug"],
            state: "open",
            htmlUrl: "https://example.test/issues/16",
            createdAt: "2026-01-01T00:00:00Z",
            updatedAt: "2026-01-01T00:00:00Z"
          },
          score: 0.33
        }
      ]
    });

    expect(comment).toContain("## 可能相关的历史 Issue");
    expect(comment).toContain("<details>");
    expect(comment).toContain("展开查看 1 个相似 Issue");
    expect(comment).toContain("https://example.test/issues/16");
    expect(comment).toContain("## Possibly Related Issues");
    expect(comment).toContain("Expand to view 1 similar issues");
  });
});
